<template>

    <li>
        <base-card>
            <header>
                <h3>
                    {{title}}

                </h3>
                <base-button mode="flat" @click="del">Delete</base-button>
            </header>
            <p>{{description}}</p>
<nav>
    <a :href="link">View resource</a>
</nav>
        </base-card>

    </li>
    
</template>

<script>
export default {
  inject:['resources'],
    props:['title', 'description', 'link', 'id'],
    methods:{
      del(){
       this.resources.forEach(e => {
         if(e.id === this.id){
           let index = this.resources.indexOf(e);
           this.resources.splice(index,1)
           console.log(index);
         }
       });
      }
    }
}
</script>
